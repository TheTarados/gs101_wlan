def get_data(packet):
    return [packet[2*i]+(packet[2*i+1]<<8) for i in range(len(packet)//2)]

def print_sit(packet, header=""):
    if packet == b'':
        return
    data = get_data(packet)
    print(header,
          MSG_TYPE.get(data[0], f"Unknown type {hex(data[0])}"),
          SIT_OPS.get(data[1], f"Unknown op {hex(data[1])}"),
          MSG_STATUS.get(data[5], f"Unknown error message {hex(data[5])}") if len(data)>5 else "No status",
          packet[12:])

def sdrc(socket, packet):
    socket.sendall(b'\xff\x00'+packet)
    # Receive response from server
    response = socket.recv(1024)
    return response


def sdrc_op(socket, op, data = b''):
    packet = b'\x00\x00' + SIT_OPS_I[op].to_bytes(2,"little") + data
    data = sdrc(socket, packet)
    return data[12:]

def string_to_bytes(input_str):
    byte_pairs = [input_str[i:i+2] for i in range(0, len(input_str), 2)]
    byte_object = [int(pair, 10) for pair in byte_pairs]
    byte_object[-1] = 0xf0 | byte_object[-1]
    return bytes(byte_object)

def string_to_7bit_bytes(s):
    packed = 0
    bit_count = 0
    byte_list = []
    
    for c in s:
        packed |= (ord(c) << bit_count)  # Shift and OR the character into packed
        bit_count += 7  # Increment bit count by 7

        while bit_count >= 8:  # Extract full bytes
            byte_list.append(packed & 0xFF)  # Store the least significant byte
            packed >>= 8  # Remove the stored byte
            bit_count -= 8  # Decrease bit count

    if bit_count:  # If there are leftover bits, store them as the last byte
        byte_list.append(packed)

    return bytes(byte_list)

def sms_data(phone_number, message):
    #expect "32123567890" as format for number. + is implied

    #unknown argument
    data = b'\x00\x08\x07\x91\x23\x94\x05\x20\x35\xf0\x00\x00\x00\x00'
    args = b'\x01\x00\x0b\x91'
    #phone number
    args += string_to_bytes(phone_number)
    args += b'\x00\x00'
    #length of message inchar
    args+= len(message).to_bytes(1, "little")
    
    args += string_to_7bit_bytes(message)
    #Length of data left in bytes
    data += len(args).to_bytes(1, "little") + args
    data += bytes(259-len(data))
    return data

def sim_channel_data(aid):
    ret = len(aid).to_bytes(1,"little")+aid
    if len(ret)<0x1e-12:
        ret += bytes(0x1e - 12 - len(ret))
    return ret


def apdu_data(channel, cla, ins, p1, p2, p3 = None, data = None):
    ret = channel.to_bytes(1, "little") + b'\x00\x00\x00'
    ret += cla.to_bytes(1, "little") + b'\x00\x00\x00'
    ret += ins.to_bytes(1, "little") + b'\x00\x00\x00'
    ret += p1.to_bytes(1, "little") + b'\x00\x00\x00'
    ret += p2.to_bytes(1, "little") + b'\x00\x00\x00'
    if p3==None:
        return ret
    ret += p3.to_bytes(1, "little") + b'\x00\x00\x00'
    ret += len(data).to_bytes(2, "little")
    ret += data
    
    ret += bytes(len(data))

    return ret

def sim_unlock_data(code):
    """
        code is passed as ASCII
    """
    ret = len(code).to_bytes(1, "little")
    ret += code.encode()
    ret += b'\x00\x00\x00\x00\x10\xa0\x00\x00\x00\x87\x10\x02\xff\x49\xff\xff\x89\x08\x15\x00\xff'
    return ret
